package com.Maarten;

public class Couch {

    // Fields

    double size;
    String color;

    // Constructors


    public Couch(double size, String color) {
        this.size = size;
        this.color = color;
    }

    // Methods


    public double getSize() {
        return size;
    }

    public String getColor() {
        return color;
    }
}
